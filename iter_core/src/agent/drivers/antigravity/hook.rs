//! Hook plumbing for [`AntigravityAgent`](super::AntigravityAgent)'s
//! interactive mode.
//!
//! Antigravity CLI uses JSON-defined lifecycle hooks (Inspect/blocking/
//! modify types). The hook directory and schema differ from Gemini CLI's
//! shell-script `AfterAgent` hook and are not yet stable or documented.
//!
//! This module is intentionally empty. Implement a `HookBundle` here
//! once the Antigravity hook JSON schema is documented and stable.
